export const getRelatedUniversities = (slug, count = 4) => {
  const current = universityData.find((u) => u.slug === slug);
  if (!current) return [];
  return universityData
    .filter((u) => u.slug !== slug)
    .sort(() => Math.random() - 0.5)
    .slice(0, count);
};
